void main() {
  // keliling persegi
  int sisi = 10;
  int keliling = sisi * 4;
  print(keliling);

  // luas lingkaran
  int jari2 = 15;
  double luas = 3.14 * jari2 * jari2;
  print(luas);

  // volume balok
  int panjang = 6;
  int lebar = 5;
  int tinggi = 30;
  int volume = panjang * lebar * tinggi;
  print(volume);
}
